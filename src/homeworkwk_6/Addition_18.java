package homeworkwk_6;
//18. Write a Java program to print the sum (addition), multiply, subtract, divide and
//remainder of two numbers.



public class Addition_18 {
    public static void main(String[] args) { //main method
        int a = 125;                    //local variable
        int b = 24;                      //local variable
        System.out.println("Addition is "+(a+b) );
        System.out.println("Multiplication is" +(a*b));
        System.out.println("Substraction is" +(a-b));
        System.out.println("Divition is"+(a/b));
        System.out.println(a +" mod " +b +" = "+(a%b));



    }
}
